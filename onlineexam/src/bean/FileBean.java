package bean;

public class FileBean {
	String filename;
	long filesize;

	public String getFilename() {
		return filename;
	}

	public void setFilename(String filename) {
		this.filename = filename;
	}

	public long getFilesize() {
		return filesize;
	}

	public void setFilesize(long l) {
		this.filesize = l;
	}

}
